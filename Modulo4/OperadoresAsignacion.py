# Operadores de asignacion
miVariable = 10
print(miVariable)

miVariable = miVariable + 1 
print(miVariable)

miVariable += 1 # Reduce el incremento de una variable
print(miVariable)

# Se puede hacer para cualquier tipo de operador
miVariable -= 2

print(miVariable)

miVariable *= 3
print(miVariable)

miVariable /= 2
print(miVariable)