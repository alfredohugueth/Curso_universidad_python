operandoA = 3
operandoB = 2

suma = operandoA + operandoB
# print('The result of the addition is:', suma)
print(f'The result of the addition is: {suma}') #Forma preferida de imprimir variables en python

resta = operandoA - operandoB
print(f'The result of the subtraction is: {resta}')

multiplicacion = operandoA * operandoB
print(f'The result of the multiplication is: {multiplicacion}')

division = operandoA / operandoB ## Retorna el resultado de la division como float
print(f'The result of the division is: {division}')

division = operandoA // operandoB ## Retorna el resultado de la division como entero
print(f'The result of the division is: {division}')

modulo = operandoA % operandoB
print(f'The module result is: {modulo}')

exponente = operandoA ** operandoB
print(f'The exponential result is: {exponente}')