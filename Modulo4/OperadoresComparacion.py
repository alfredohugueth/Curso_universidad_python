# Operadores de comparacion
a = 4
b = 2

resultado = (a == b)
print(f'Resultado == : {resultado}')

resultado = (a != b)
print(f'Resultado != : {resultado}')

resultado = (a > b)
print(f'Resultado > : {resultado}')

resultado = (a >= b) # Mayor o igual
print(f'Resultado >= : {resultado}')

resultado = (a <= b)
print(f'Resultado <= : {resultado}')

resultado = (a < b)
print(f'Resultado < : {resultado}')

