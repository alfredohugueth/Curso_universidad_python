# Operador + 
a = 2
b = 3

print( a + b ) # suma de los dos valores

a = 'Hola'
b = 'Mundo'

print( a + b ) # Resultado Hola Mundo

a = [1,2,3]
b = [6,7,8]

print( a + b ) # Genera la union de ambas listas

